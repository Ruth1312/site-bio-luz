    <div id="titulo">
      <h2>Quais são os resultados atingidos?</h2>
    </div>
    <br>

    <p>
      A partir dessa avaliação, imagens em 2D e em 3D são geradas e, então, interpretadas pelo radiologista, que emite
      laudos sobre a composição da parte do corpo investigada. Em tons de cinza, podem aparecer massas, lesões ou
      presença de líquidos, assim como alterações na estrutura, altura e composição de órgãos e tecidos moles.
    </p>
