    <div id="titulo">
      <h2>O que é o ultrassom?</h2>
    </div>

    <br>

    <p>
      Também chamado de ultrassonografia e ecografia, o ultrassom é um exame de imagem realizado por um transdutor,
      aquele aparelhinho que o médico encosta na pele da pessoa e que emite e capta ondas sonoras por meio do contato
      com o corpo humano. Com base nessa avaliação, imagens são formadas e investigadas por um médico.
      Prático, acessível e sem nenhum efeito colateral, é amplamente utilizado no diagnóstico e acompanhamento de uma
      série de condições.
    <div class="figure">
      <iframe width="500" height="350" src="https://www.youtube.com/embed/GcPvhTuz8cs" frameborder="0" allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
      <figcaption>Entenda como funciona a ultrassom</figcaption>
    </div>

    </p>
    <div id="titulo">
      <h2>Para que ele serve?</h2>
    </div>

    <br>
    <p>
      Por ser inofensivo, o exame é usado principalmente em crianças e gestantes, seja como parte do pré-natal ou no
      rastreamento de males em recém-nascidos. Mas não só isso.

      O teste também é empregado para traçar uma primeira avaliação do estado de órgãos internos, como o fígado e os
      rins. Nas mulheres, o ultrassom transvaginal analisa o útero, ovários e colo uterino.
      Suspeitas de infecções como a apendicite, doenças que afetam a tireoide e os vasos sanguíneos, cálculos na
      vesícula e nos rins também são detectados em poucos minutos. Há ainda a possibilidade de analisar o estrago de
      lesões ortopédicas em tecidos moles, como ligamentos e cartilagem… Ufa!
    </p>
    <div class="figure">
      <img src="imagens\baby2.gif" class="imgy" width="500" height="350" alt="ultrassom">
      <figcaption>Ultrassonografia sendo feita</figcaption>
    </div>



    <div id="titulo">
      <h2>Como ele é feito?</h2>
    </div>
    <br>

    <p>
      A pessoa é posicionada na maca – a parte do corpo a ser examinada pelo ultrassom não pode estar com roupas. É
      importante também que não haja ar entre o transdutor e a pele, o que comprometeria a qualidade das imagens. Para
      garantir, o médico radiologista lambuza o aparelho e próprio paciente com gel.

      Durante o exame, que dura cerca de 30 minutos (o tempo varia conforme a aplicação), o dispositivo “troca sons”
      com o corpo. Como assim? Ora, as ondas sonoras entram e os ecos de sua passagem pelo corpo são “escutados” pelo
      computador, que transforma os sinais em fotos ou vídeos.
    </p>


    <div class="figure">
